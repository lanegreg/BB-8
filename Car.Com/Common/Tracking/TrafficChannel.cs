    #region - Methods to infer TrafficChannel from the referrer_url and affiliate group name

    private static bool AffiliateIsValidAndNotTheDefault(IAffiliate affiliate)
    {
      return affiliate != null && !DefaultAffiliateIds.Contains(affiliate.Id);
    }

    private static bool AffiliateIsValidAndAnAutobytelAsset(IAffiliate affiliate)
    {
      return affiliate != null && DefaultAffiliateIds.Contains(affiliate.Id);
    }

    private static bool ReferrerIsKnownAndNotUs(string referrerHost, string thisHost)
    {
      return referrerHost.IsNotNullOrEmpty() && referrerHost != thisHost;
    }

    private static bool ReferrerIsASocialMediaChannel(string referrerHost)
    {
      return TestReferrerAgainstDomainList(referrerHost, _socialMediaDomains);
    }

    private static bool ReferrerIsASearchMediaChannel(string referrerHost)
    {
      return TestReferrerAgainstDomainList(referrerHost, _seoDomains);
    }

    private static bool TestReferrerAgainstDomainList(string referrerHost, IList<string> domains)
    {
      var domainHasBeenFound = domains.Any(referrerHost.Equals);
      var segments = referrerHost.Split('.').Reverse().ToList();

      if (!domainHasBeenFound && segments.Count() >= 2)
      {
        domainHasBeenFound = domains.Any(String.Format("{1}.{0}",
                                                       segments.ElementAt(0),
                                                       segments.ElementAt(1)).Equals);
      }

      if (!domainHasBeenFound && segments.Count() >= 3)
      {
        domainHasBeenFound = domains.Any(String.Format("{2}.{1}.{0}",
                                                       segments.ElementAt(0),
                                                       segments.ElementAt(1),
                                                       segments.ElementAt(2)).Equals);
      }

      if (!domainHasBeenFound && segments.Count() >= 4)
      {
        domainHasBeenFound = domains.Any(String.Format("{3}.{2}.{1}.{0}",
                                                       segments.ElementAt(0),
                                                       segments.ElementAt(1),
                                                       segments.ElementAt(2),
                                                       segments.ElementAt(3)).Equals);
      }

      if (!domainHasBeenFound && segments.Count() == 5)
      {
        domainHasBeenFound = domains.Any(String.Format("{4}.{3}.{2}.{1}.{0}",
                                                       segments.ElementAt(0),
                                                       segments.ElementAt(1),
                                                       segments.ElementAt(2),
                                                       segments.ElementAt(3),
                                                       segments.ElementAt(4)).Equals);
      }

      return domainHasBeenFound;
    }

